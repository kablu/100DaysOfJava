/*
 * public boolean isEmpty()
 *  
 * Returns: 
 * -------
 * true if length() is 0, otherwise false
 */
public class IsEmptyDemo
{
	public static void main(String[] args)
	{
		String s1 = "";
		String s2 = "welcome";

		System.out.println(s1.isEmpty());//true
		System.out.println(s2.isEmpty());//false

	}
}