public class Dogleg
{
		private int numberOflegs;

		public int getNumberOflegs()
		{
				return numberOflegs;
		}

		public void setNumberOflegs( int numberOflegs )
		{
				this.numberOflegs = numberOflegs;
		}

		@Override
        public String toString()
        {
		        return "Dogleg [numberOflegs=" + numberOflegs + "]";
        }

		
}
