public class Dogear
{
		private int numberOfEars;

		public int getNumberOfEars()
		{
				return numberOfEars;
		}

		public void setNumberOfEars( int numberOfEars )
		{
				this.numberOfEars = numberOfEars;
		}

		@Override
        public String toString()
        {
		        return "Dogear [numberOfEars=" + numberOfEars + "]";
        }

		
}
